export const toTimeLabel = (time: string): string => `${time.slice(0, 2)}:${time.slice(2)}`;

export const filterTimeOptions = (
  timeOptions: string[],
  selectedTime: string,
  filter: 'greater' | 'lesser',
) =>
  filter === 'greater'
    ? timeOptions.filter((option) => option > selectedTime)
    : timeOptions.filter((option) => option < selectedTime);
